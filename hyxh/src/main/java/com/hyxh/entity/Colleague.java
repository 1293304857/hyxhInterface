package com.hyxh.entity;

public class Colleague {
	private String ColleagueId;
	private String ColleagueName;
	private String ColleaguePosition;
	private String ColleagueMobile;
	private String ColleagueEmali;
	private String signupId;
	public String getColleagueId() {
		return ColleagueId;
	}
	public void setColleagueId(String colleagueId) {
		ColleagueId = colleagueId;
	}
	public String getColleagueName() {
		return ColleagueName;
	}
	public void setColleagueName(String colleagueName) {
		ColleagueName = colleagueName;
	}
	public String getColleaguePosition() {
		return ColleaguePosition;
	}
	public void setColleaguePosition(String colleaguePosition) {
		ColleaguePosition = colleaguePosition;
	}
	public String getColleagueMobile() {
		return ColleagueMobile;
	}
	public void setColleagueMobile(String colleagueMobile) {
		ColleagueMobile = colleagueMobile;
	}
	public String getColleagueEmali() {
		return ColleagueEmali;
	}
	public void setColleagueEmali(String colleagueEmali) {
		ColleagueEmali = colleagueEmali;
	}
	public String getSignupId() {
		return signupId;
	}
	public void setSignupId(String signupId) {
		this.signupId = signupId;
	}
	
}
